<script>
export default {
  name: "link-start",
  props:{
    visible:{
      type:Boolean,
      default(){
        return false
      }
    },
  },
  data(){
    return{
      show:false,
      firstDelay:1000,
      secondDelay:1000+3500,
    }
  },
  methods:{
    beforeStart(){
      this.$refs.LinkStart.play();
    },
    Start(){
      this.$refs.NerveGear.play();
      this.show = this.visible;
    },
    End(){
      this.$refs.Welcome.play();
      this.$emit('update:visible',false)
      this.show = false;
    },
    saoStart(){
      this.beforeStart();
      let timer1 = null;
      timer1 = setTimeout(()=>{
        this.Start();
        timer1 = null;
      },this.firstDelay);
      let timer2 = null;
      timer2 = setTimeout(()=>{
        this.End();
        timer2 = null;
      },this.secondDelay);

      this.$once("hook:beforeDestroy", () => {
        clearInterval(timer1);
        clearInterval(timer2);
      });
    }
  },
  watch:{
    visible:function (val){
      if(val){
        this.saoStart();
      }
    }
  },
  mounted() {
    if(this.visible){
      this.saoStart();
    }
  }
}
</script>

